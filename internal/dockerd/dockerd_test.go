func TestManager_ReadPid(t *testing.T) {
	tmpDir := t.TempDir()
	pidFile := filepath.Join(tmpDir, "test.pid")
	require.NoError(t, os.WriteFile(pidFile, []byte("12345\n"), 0644))

	m := &Manager{}
	pid, err := m.readPid(pidFile)
	require.NoError(t, err)
	assert.Equal(t, 12345, pid)
}

func TestManager_ReadPid_Missing(t *testing.T) {
	m := &Manager{}
	_, err := m.readPid("/nonexistent.pid")
	require.Error(t, err)
}

func TestManager_ReadPid_Garbage(t *testing.T) {
	tmpDir := t.TempDir()
	pidFile := filepath.Join(tmpDir, "test.pid")
	require.NoError(t, os.WriteFile(pidFile, []byte("not-a-number"), 0644))

	m := &Manager{}
	_, err := m.readPid(pidFile)
	require.Error(t, err)
	assert.Contains(t, err.Error(), "parsing pid")
}
