package dockerd

import (
	"os"
	"path/filepath"
	"testing"

	"github.com/stretchr/testify/assert"
	"github.com/stretchr/testify/require"
)

func TestParseResolvConf(t *testing.T) {
	tmpDir := t.TempDir()
	path := filepath.Join(tmpDir, "resolv.conf")

	content := `# Generated by NetworkManager
search corp.local
nameserver 10.0.0.53
nameserver 8.8.8.8
options edns0
`
	require.NoError(t, os.WriteFile(path, []byte(content), 0644))

	servers := parseResolvConf(path)
	assert.Equal(t, []string{"10.0.0.53", "8.8.8.8"}, servers)
}

func TestParseResolvConf_MissingFile(t *testing.T) {
	got := parseResolvConf("/nonexistent/resolv.conf")
	assert.Nil(t, got)
}

func TestParseResolvConf_NoNameservers(t *testing.T) {
	tmpDir := t.TempDir()
	path := filepath.Join(tmpDir, "resolv.conf")
	require.NoError(t, os.WriteFile(path, []byte("search example.com\n"), 0644))

	got := parseResolvConf(path)
	assert.Empty(t, got)
}

